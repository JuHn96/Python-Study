import numpy as np

arr1 = np.array([1, 2, 3, 4, 5])
arr2 = np.array([10, 20, 30, 40, 50])

# 1. 합
print(arr1 + arr2)  # [11 22 33 44 55]

# 2. 곱
print(arr1 * arr2)  # [10 40 90 160 250]

# 3. arr1에 10 더하기
print(arr1 + 10)  # [11 12 13 14 15]

# 4. arr2를 2로 나누기
print(arr2 / 2)  # [5. 10. 15. 20. 25.]